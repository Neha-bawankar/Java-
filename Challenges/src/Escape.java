public class Escape {
    public static void main(String[] args) {

       /* /n */
        System.out.println("My Name is \nNeha");

        /* /b */
        System.out.println("My Name is \bNeha");


        /* /t */
        System.out.println("My Name is \tNeha");

        /* /' */
        System.out.println("My Name is \'Neha\'");

        /* /" */
        System.out.println("My Name is \"Neha\"");
    }
}
